class Figurate:

    def triangle(self, n):
        return n * (n + 1) / 2

    def square(self, n):
        return n ** 2

    def pentagonal(self, n):
        return n * ((3 * n) - 1) / 2

    def hexagonal(self, n):
        return n * ((2 * n) - 1)

    def heptagonal(self, n):
        return n * ((5 * n) - 3) / 2

    def octoganol(self, n):
        return n * ((3 * n) - 2)

